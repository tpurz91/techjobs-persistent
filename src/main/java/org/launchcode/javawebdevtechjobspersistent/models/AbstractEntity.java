package org.launchcode.javawebdevtechjobspersistent.models;

import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import javax.persistence.MappedSuperclass;
import javax.validation.constraints.NotBlank;
import javax.validation.constraints.Size;
import java.util.Objects;

@MappedSuperclass              // *** add @MappedSuperClass annotation ***
public abstract class AbstractEntity {

    @Id                        // *** add @Id & @GeneratedValue ***
    @GeneratedValue
    private int id;

    @NotBlank(message = "Name is required")        // *** add validation & limit on size of name ***
    @Size(min = 3, max = 75, message = "Must be between 3 and 75 characters")

    private String name;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return name;
    }

    @Override
    public boolean equals(Object o) {
        if (this == o) return true;
        if (o == null || getClass() != o.getClass()) return false;
        AbstractEntity that = (AbstractEntity) o;
        return id == that.id;
    }

    @Override
    public int hashCode() {
        return Objects.hash(id);
    }

}
